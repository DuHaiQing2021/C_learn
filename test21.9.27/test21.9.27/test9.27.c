int main()
{
	int a = 3;    //00000000000000000000000000000011
	int b = -5;   //11111111111111111111111111111011
	                                                                                     
	a = a ^ b;    //00000000000000000000000000000011--a的补码  
				  //11111111111111111111111111111011--b的补码
	              //11111111111111111111111111111000--a^b的补码
	              //10000000000000000000000000001000--a^b的原码（-8）
	              
	b = a ^ b;    //11111111111111111111111111111000--a的补码
	              //11111111111111111111111111111011--b的补码
				  //00000000000000000000000000000011--a^b的补码
	              //00000000000000000000000000000011--a^b的原码（3）

	a = a ^ b;    //11111111111111111111111111111000 a的补码
	              //00000000000000000000000000000011 b的补码
	              //11111111111111111111111111111011 a^b的补码
	              //10000000000000000000000000000101 a^b的原码（-5）
	              
	printf("%d %d\n", a, b);
	return 0;
}